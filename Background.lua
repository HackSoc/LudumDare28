function Background:initialize()
    for i = 0, 156 do
        k = 0
        if i > 11 then
            k = -1
        end
        self.tile[i+1+k] = love.graphics.newImage("assets/tiles"..i..".png") 
    end
end

function Background:draw()
    for y = 0, 8 do
        for x = 0, self.mapWidth-1 do
            tileNo = self.map[y*self.mapWidth+x+1]
            
            if tileNo ~= 0 then
                love.graphics.draw(self.tile[tileNo], x * 70 - self.drawX, y * 70)
            end
        end
    end
end

function Background:moveX(newX)
    self.drawX = 100
end
